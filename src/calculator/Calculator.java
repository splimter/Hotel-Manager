    private void btnEqlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqlActionPerformed
        // TODO add your handling code here:
        numA = Float.valueOf(Display.getText().replace(",", "."));

        switch (lastOpr.toString()) {
            case "plus":
                numB += numA;
                break;
            case "min":
                numB -= numA;
                break;
            case "mul":
                numB *= numA;
                break;
            case "div":
                if (numA == 0) {
                    break;
                } else {
                    numB /= numA;
                }
                break;
        }
        Display.setText(String.valueOf(numB));
        isCLS = true;
        DevABLog();
    }//GEN-LAST:event_btnEqlActionPerformed

