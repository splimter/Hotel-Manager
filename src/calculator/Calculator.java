    void Switcher() {
        float _f = Float.valueOf(Display.getText().replace(",", "."));
        switch (num.toString()) {
            case "A":
                num = Num.B;
                numA = _f;
                break;
            case "B":
                numB = _f;
                Calcule(lastOpr);
                numA = numB;
                break;
        }
    }

    void Calcule(Opr o) {
        switch (lastOpr.toString()) {
            case "plus":
                numB += numA;
                break;
            case "min":
                numB -= numA;
                break;
            case "mul":
                numB *= numA;
                break;
            case "div":
                if (numA == 0) {
                    break;
                } else {
                    numB /= numA;
                }
                break;
        }
        Display.setText(String.valueOf(numB));
    }

